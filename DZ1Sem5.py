# Создайте программу для игры с конфетами человек против человека.
# Условие задачи: На столе лежит 2021 конфета. Играют два игрока делая ход
# друг после друга. Первый ход определяется жеребьёвкой.
# За один ход можно забрать не более чем 28 конфет.
# Все конфеты оппонента достаются сделавшему последний ход.
# Сколько конфет нужно взять первому игроку, чтобы забрать все конфеты у своего конкурента?


import random

print('На столе лежит 2021 конфета. Играют два игрока делая ход друг после друга. Первый ход определяется жеребьёвкой. За один ход можно забрать не более чем 28 конфет. Все конфеты оппонента достаются сделавшему последний ход.')

# Определяем первого игрока
player1 = input('Введите имя первого игрока: ')
player2 = input('Введите имя второго игрока: ')

# Определяем первого ходить игрока с помощью жребия
coin_toss = random.randint(1, 2)

if coin_toss == 1:
    print(f'{player1} первый ходит.')
    current_player = player1
else:
    print(f'{player2} первый ходит.')
    current_player = player2

# Начинаем игру
candies = 2021
while candies > 0:
    if current_player == player1:
        print(f'У {player1} осталось {candies} конфет.')
        take = int(input('Сколько конфет вы хотите взять? '))
        if take > 28 or take > candies:
            print('Вы можете взять не более 28 конфет за ход.')
        else:
            candies -= take
            current_player = player2
    else:
        take = 28
        if take > candies:
            take = candies
        print(f'{player2} взял {take} конфет.')
        candies -= take
        current_player = player1

if current_player == player1:
    print(f'{player1} выиграл!')
else:
    print(f'{player2} выиграл!')